
function dodawanie(a,b,i){
  let wynik = parseInt(a,i) + parseInt(b,i);
  let wwynik = wynik.toString(i);
  return wwynik;
}

function odejmowanie(a,b,i){
  let wynik = parseInt(a,i) - parseInt(b,i);
  let wwynik = wynik.toString(i);
  return wwynik;
}

function dzielenie(a,b,i){
  let wynik = parseInt(a,i) / parseInt(b,i);
  let wwynik = wynik.toString(i);
  return wwynik;
}

function mnozenie(a,b,i){
  let wynik = parseInt(a,i) * parseInt(b,i);
  let wwynik = wynik.toString(i);
  return wwynik;
}
